def checkSpinProjectionSystem() -> None:
    def invPerm(p: tuple[int, ...]) -> tuple[int, ...]:
        out = [0] * len(p)

        for i, x in enumerate(p):
            out[x] = i

        return tuple(out)

    def compose(p: tuple[int, ...], q: tuple[int, ...]) -> tuple[int, ...]:
        return tuple(p[q[i]] for i in range(len(p)))

    def cycleCount(p: tuple[int, ...]) -> int:
        seen = [False] * len(p)
        cycles = 0

        for i in range(len(p)):
            if seen[i]:
                continue

            cycles += 1
            j = i

            while not seen[j]:
                seen[j] = True
                j = p[j]

        return cycles

    for rank in (2, 3, 4):
        perms = tuple(permutations(range(rank)))

        for upperSpins in cartesianProduct(SPINS, repeat = rank):
            for lowerSpins in cartesianProduct(SPINS, repeat = rank):
                coeffs = dict(Spin.coeffs(rank, upperSpins, lowerSpins))

                for p in perms:
                    lhs = Fraction(0)

                    for q in perms:
                        rel = compose(invPerm(p), q)
                        lhs += (
                            coeffs.get(q, Fraction(0))
                            * permutationSign(p)
                            * permutationSign(q)
                            * Fraction(2 ** cycleCount(rel), 1)
                        )

                    rhs = (
                        Fraction(permutationSign(p), 1)
                        if all(upperSpins[i] == lowerSpins[p[i]] for i in range(rank))
                        else Fraction(0)
                    )

                    assertEq(
                        lhs,
                        rhs,
                        f"spin projection system rank {rank} {upperSpins} {lowerSpins} {p}",
                    )

