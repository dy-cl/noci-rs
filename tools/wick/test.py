            C(1, 2, T(t, s), L3(p, r, v, q, u, w)),
            C(1, 2, T(t, q), L3(p, r, v, u, s, w)),

            C(-1, 2, G(r, u), L3(p, t, v, q, s, w)),
            C(-1, 2, G(r, w), L3(p, t, v, q, u, s)),
