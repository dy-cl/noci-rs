def G(p: Idx, q: Idx) -> Expr:
    return tensor("Gamma1", (p,), (q,))

def T(p: Idx, q: Idx) -> Expr:
    return tensor("Theta", (p,), (q,))

def D(p: Idx, q: Idx) -> Expr:
    return delta(p, q)

def L2(p: Idx, q: Idx, r: Idx, s: Idx) -> Expr:
    return tensor("Lambda2", (p, q), (r, s))

def L3(p: Idx, q: Idx, r: Idx, s: Idx, t: Idx, u: Idx) -> Expr:
    return tensor("Lambda3", (p, q, r), (s, t, u))

def L4(
    p: Idx,
    q: Idx,
    r: Idx,
    s: Idx,
    t: Idx,
    u: Idx,
    v: Idx,
    w: Idx,
) -> Expr:
    return tensor("Lambda4", (p, q, r, s), (t, u, v, w))

def orbitProjectsToZero(rank: int, coeffs: dict[tuple[int, ...], Fraction]) -> bool:
    """Return True if a Lambda rank-k orbit lies in the spin-null space.

    Notation:
        G c = 0

    Examples:
        For rank 3 this recognises

            \sum_{\pi \in S_3} \Lambda^{pqr}_{\pi(abc)} = 0

        For rank 4 it recognises the corresponding Lambda4 spin-null
        relations generated by the rank-4 spin-projection Gram matrix.
    """
    perms = tuple(permutations(range(rank)))
    gram = spinGram(rank)

    for i, _p in enumerate(perms):
        lhs = Fraction(0)

        for j, q in enumerate(perms):
            lhs += gram[i][j] * coeffs.get(q, Fraction(0))

        if lhs != 0:
            return False

    return True

def reduceSpinNull(expr: Expr) -> Expr:
    """Remove Lambda3/Lambda4 spin-null orbit differences for tests.

    Notation:
        Two expressions are equal if their difference lies in the spin-null
        space of the corresponding spin-free cumulant projection.

    Examples:
        Lambda3 full lower-permutation orbit is removed.

        Lambda4 null combinations are removed by the same G c = 0 condition.

    This is not a block-specific rule. It applies to any Lambda3 or Lambda4
    term with fixed upper indices, fixed lower-index set, and fixed surrounding
    scalar factors.
    """
    grouped: dict[tuple, dict[tuple[int, ...], Fraction]] = {}
    passthrough = []

    for term in expr:
        high = [
            tensorIn
            for tensorIn in term.tensors
            if lambdaRank(tensorIn.name) in (3, 4)
        ]

        if len(high) != 1:
            passthrough.append(term)
            continue

        lam = high[0]
        rank = lambdaRank(lam.name)

        if rank is None:
            passthrough.append(term)
            continue

        if len(lam.upper) != rank or len(lam.lower) != rank:
            passthrough.append(term)
            continue

        if len(set(lam.lower)) != rank:
            passthrough.append(term)
            continue

        lowerBase = tuple(sorted(lam.lower))
        lowerPerm = tuple(
            lowerBase.index(idx)
            for idx in lam.lower
        )

        otherTensors = []
        removed = False

        for tensorIn in term.tensors:
            if tensorIn == lam and not removed:
                removed = True
                continue

            otherTensors.append(tensorIn)

        key = (
            rank,
            term.deltas,
            tuple(otherTensors),
            lam.name,
            lam.upper,
            lowerBase,
        )

        if key not in grouped:
            grouped[key] = {}

        grouped[key][lowerPerm] = grouped[key].get(lowerPerm, Fraction(0)) + term.coeff

    out = list(passthrough)

    for key, coeffs in grouped.items():
        rank, deltas, otherTensors, name, upper, lowerBase = key

        if orbitProjectsToZero(rank, coeffs):
            continue

        for perm, coeff in sorted(coeffs.items()):
            if coeff == 0:
                continue

            out.append(
                Term(
                    coeff = coeff,
                    deltas = deltas,
                    tensors = tuple(sorted(
                        otherTensors
                        + (
                            Tensor(
                                name,
                                upper,
                                tuple(lowerBase[i] for i in perm),
                            ),
                        )
                    )),
                )
            )

    return combine(tuple(out))

def normalForm(expr: Expr) -> Expr:
    return reduceSpinNull(
        canonicaliseTensorSymmetry(
            combine(expr)
        )
    )

def isExpr(value) -> bool:
    return isinstance(value, tuple) and all(
        isinstance(term, Term)
        for term in value
    )

def assertEq(got, want, label: str) -> None:
    if isExpr(got) and isExpr(want):
        diff = normalForm(
            add(
                got,
                scale(want, Fraction(-1, 1)),
            )
        )

        if diff:
            print(f"===== {label} got =====")
            print(got)
            print(f"===== {label} want =====")
            print(want)
            print(f"===== {label} diff =====")
            print(diff)
            raise SystemExit(1)

        return

    if got != want:
        print(f"===== {label} got =====")
        print(got)
        print(f"===== {label} want =====")
        print(want)
        raise SystemExit(1)

def checkCar() -> None:
    u = A("u")
    v = A("v")

    assertEq(
        anticommutator(
            Op("annihilate", u, ALPHA, 0),
            Op("create", v, ALPHA, 1),
        ),
        delta(u, v),
        "CAR same spin",
    )

    assertEq(
        anticommutator(
            Op("annihilate", u, ALPHA, 0),
            Op("create", v, BETA, 1),
        ),
        zero(),
        "CAR opposite spin",
    )

